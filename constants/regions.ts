export function formatRegion(city: string, area: string): string {
  return `${city} ${area}`;
}

export function parseRegion(region: string | null): { city: string; area: string } | null {
  if (!region) return null;
  const [city, ...rest] = region.split(' ');
  return { city, area: rest.join(' ') };
}
